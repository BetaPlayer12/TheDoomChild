            RaycastHit2D[] hit = Cast(transform.position, Vector2.down, 1000, true, out hitCount, true);
            //var hitPos = (new Vector2(m_projectilePoint.position.x, Vector2.down.y) * hit[0].distance);
            //return hitPos;
            return hit[0].point;
        }

